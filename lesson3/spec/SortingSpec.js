describe("Problems", function () {
  let Sorting = require('../src/sorting/Sorting');
  console.log("\n-----------------------------\n");
  console.log("Now testing Sorting.js!");

  it("should use insertion sort in..", function () {
    expect(Sorting.insertionSort([5, 9, 2, 1, 3, 8, 7]))
    .toEqual([1, 2, 3, 5, 7, 8, 9]);
  });

  it("should use bubble sort in..", function () {
    expect(Sorting.bubbleSort([5, 9, 2, 1, 3, 8, 7]))
    .toEqual([1, 2, 3, 5, 7, 8, 9]);
  });

  it("should use merge sort in..", function () {
    expect(Sorting.mergeSort([5, 9, 2, 1, 3, 8, 7]))
    .toEqual([1, 2, 3, 5, 7, 8, 9]);
  });

  it("should also merge sort a 1 element array", function () {
    expect(Sorting.mergeSort([7])).toEqual([7]);
  });
});
